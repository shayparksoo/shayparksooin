            <h3>Brainstorming & Concept</h3>
            <p class="card-body padding-49-r">I researched the existing pieces of software to see how they process the user flow and interaction through UI. From the research, I found out that platforms such as Square and Shopify tend to be <b>simple</b> to support usability. This is to aid users who need to look at various sections in a short amount of time.</p>
            <p class="card-body padding-49-r p-break">Also, given that restaurants are in a busy environment, two things were considered to help this. First, photos were used as a guided action to discover the menus faster for users. As images might not be professionally taken or have many ingredients that might cause distraction, the UI was considered simpler and more <b>distinctive</b> from the graphics. Second, UI should take as <b>less space</b> as it could to give maximum visibility of the menu items. This will also help older users to find items easily.</p>
