            <h3>Concept & Ideas</h3>
            <p class="card-body padding-49-r p-break">The target audience of the application was to aim for users who have debit or credit cards for subscription use. The age is targeted to be between 16 to 40 who actively use subscriptions in daily basis. As the age gap is wide, the concept of the design was set to be <b>clean</b> and <b>less dynamic</b> to increase the <b>usability</b>.</p>
            <p class="card-body padding-49-r p-break">I researched <a href="https://m3.material.io/" title="Go to Material Design 3">Material Design 3</a> to reference their features such as navigation bar and input field. My design uses a mixture of iOS and Android UI design because there were <b>advantages of both design system</b> that my design could benefit (i.e. bottom navigation has both floating action and a setting page).</p>
